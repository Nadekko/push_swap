#include "push_swap.h"

long	ft_atol(const char *nptr)
{
	long	res;
	int		neg;

	res = 0;
	neg = 1;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == 32)
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			neg *= -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		res = res * 10 + (*nptr - 48);
		nptr++;
	}
	if (res < INT_MIN || res > INT_MAX)
	{
		ft_perror("Error\n");
		return (-1);
	}
	return (res * neg);
}
